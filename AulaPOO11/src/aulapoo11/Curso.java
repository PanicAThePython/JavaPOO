package aulapoo11;
public class Curso {
    
    private String nome;
    private int numFases;
    private float valMensal;

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getNumFases() {
        return numFases;
    }
    public void setNumFases(int numFases) {
        this.numFases = numFases;
    }

    public float getValMensal() {
        return valMensal;
    }
    public void setValMensal(float valMensal) {
        this.valMensal = valMensal;
    }
    
    
}
